/*******************************************************
 * Repräsentiert die Tastatureingaben für das Spiel. 
 * Jede Eigenschaft steht für eine bestimmte Taste und 
 * ist true, solange diese Taste gedrückt ist.
 *******************************************************/
class Keyboard {

    /**
     * Gibt an, ob die linke Pfeiltaste gedrückt ist.
     * @type {boolean}
     */
    LEFT = false;

    /**
     * Gibt an, ob die rechte Pfeiltaste gedrückt ist.
     * @type {boolean}
     */
    RIGHT = false;

    /**
     * Gibt an, ob die Pfeiltaste nach oben gedrückt ist.
     * @type {boolean}
     */
    UP = false;

    /**
     * Gibt an, ob die Pfeiltaste nach unten gedrückt ist.
     * @type {boolean}
     */
    DOWN = false;

    /**
     * Gibt an, ob die Leertaste (SPACE) gedrückt ist.
     * @type {boolean}
     */
    SPACE = false;

    /**
     * Gibt an, ob die Taste 'D' gedrückt ist.
     * @type {boolean}
     */
    D = false;
}
